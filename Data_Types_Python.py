# Testing integer in Python. Output will be: <class 'int'>
a=1 # This is a variable where 'a' is the variable identifier, '=' is the operator, '1' is the value.
print(type(547256454))
print(type(-1))
print(type(a))
# Testing float in Python. Output will be: <class 'float'>
b=43699.357956454 # This is a variable where 'b' is the variable identifier, '=' is the operator, '43699.357956454' is the value
print(type(003.14159))
print(type(-438.69595))
print(type(b))
# Testing string in Python. Output will be: <class 'str'>
c='I love Python' # This is a variable where 'c' is the variable identifier, '=' is the operator, 'I love Python' is the value
print(type('I am S. Soorya'))
print(type('Friends'))
print(type(c))

